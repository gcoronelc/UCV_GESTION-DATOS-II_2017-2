package pe.egcc.app.model;

import java.util.Date;

public class Obligacion {

  private Integer obligacion;
  private Double importe;
  private Date fecven;
  private Integer estudiante;
  private Integer matricula;
  private Integer tobligacion;
  private Integer audempleado;
  private Date audfecha;

  public Obligacion() {
  }

  public Integer getObligacion() {
    return obligacion;
  }

  public void setObligacion(Integer obligacion) {
    this.obligacion = obligacion;
  }

  public Double getImporte() {
    return importe;
  }

  public void setImporte(Double importe) {
    this.importe = importe;
  }

  public Date getFecven() {
    return fecven;
  }

  public void setFecven(Date fecven) {
    this.fecven = fecven;
  }

  public Integer getEstudiante() {
    return estudiante;
  }

  public void setEstudiante(Integer estudiante) {
    this.estudiante = estudiante;
  }

  public Integer getMatricula() {
    return matricula;
  }

  public void setMatricula(Integer matricula) {
    this.matricula = matricula;
  }

  public Integer getTobligacion() {
    return tobligacion;
  }

  public void setTobligacion(Integer tobligacion) {
    this.tobligacion = tobligacion;
  }

  public Integer getAudempleado() {
    return audempleado;
  }

  public void setAudempleado(Integer audempleado) {
    this.audempleado = audempleado;
  }

  public Date getAudfecha() {
    return audfecha;
  }

  public void setAudfecha(Date audfecha) {
    this.audfecha = audfecha;
  }
  
  

}
