    @Override
    public int print(Graphics graphics, PageFormat pageFormat, int pageIndex) throws PrinterException {
        try
    {
        if (pageIndex > 0)
        { 
          return NO_SUCH_PAGE;
        }
      Graphics2D g2d = (Graphics2D)graphics;
      g2d.translate(pageFormat.getImageableX(), pageFormat.getImageableY());  
      FontMetrics fm = g2d.getFontMetrics();
      int anchoMayor = fm.stringWidth("Nro de Cuenta: ");      
      int posx = 50;
      int posy = 50;
      g2d.drawString("Comision de Agua Potable, Alcantarillado y Saneamiento ", posx, 40);
      g2d.drawLine(0, posy, (int)pageFormat.getImageableWidth(), posy);
      posy +=15;
      g2d.drawString("Datos del Cliente", posx, posy);
      g2d.drawLine(0, posy+3, (int)pageFormat.getImageableWidth(), posy+3);
      posy +=15;
      g2d.drawString("Nombre: "+this.orden.getCliente().getNombre(), posx+getDiferenciaAncho(anchoMayor,fm.stringWidth("Nombre: ")), posy);
      posy +=15;
      g2d.drawString("Direccion: "+this.orden.getCliente().getDireccion(), posx+getDiferenciaAncho(anchoMayor,fm.stringWidth("Direccion: ")), posy);
      posy +=15;
      g2d.drawString("Colonia: "+this.orden.getCliente().getColonia(), posx+getDiferenciaAncho(anchoMayor,fm.stringWidth("Colonia: ")), posy);
      posy +=15;
      g2d.drawString("Municipio: "+this.orden.getCliente().getMunicipio(), posx+getDiferenciaAncho(anchoMayor,fm.stringWidth("Municipio: ")), posy);
      posy +=15;
      g2d.drawString("Telefono: "+this.orden.getCliente().getTelefono(), posx+getDiferenciaAncho(anchoMayor,fm.stringWidth("Telefono: ")), posy);
      posy +=15;
      g2d.drawString("RFC: "+this.orden.getCliente().getRfc(), posx+getDiferenciaAncho(anchoMayor,fm.stringWidth("RFC: ")), posy);
            posy +=15;      
      g2d.drawString("Inspector: "+this.orden.getInspector(), posx+getDiferenciaAncho(anchoMayor,fm.stringWidth("Inspector: ")), posy);
      posy +=15;
      g2d.drawString("Tipo Orden: "+this.orden.getTipo(), posx+getDiferenciaAncho(anchoMayor,fm.stringWidth("Tipo Orden: ")), posy);
      posy +=15;
      g2d.drawLine(0, posy+3, (int)pageFormat.getImageableWidth(), posy+3);
      g2d.drawString("Motivo", posx, posy);
      g2d.drawLine(0, posy+3, (int)pageFormat.getImageableWidth(), posy+3);
      posy +=15;
      g2d.drawString("Motivo: "+this.orden.getMotivoCorte(), posx+getDiferenciaAncho(anchoMayor,fm.stringWidth("Motivo: ")), posy);
     
     
           
      //this.printAll(graphics);
      return PAGE_EXISTS;
    }
    catch(Exception ex)
        {
            JOptionPane.showMessageDialog( null, "NO HAY OBJETOS A IMPRIMIR", "ERROR DEIMPRESION", JOptionPane.ERROR_MESSAGE);
        }
    return PAGE_EXISTS;
    }
